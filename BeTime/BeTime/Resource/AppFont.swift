import UIKit

extension UIFont {

  static let appTitleFont: UIFont = {
    return UIFont.systemFont(ofSize: 25)
  }()

  static let appSubTitleFont: UIFont = {
    return UIFont.systemFont(ofSize: 20)
  }()

  static let appValueFont: UIFont = {
    return UIFont.systemFont(ofSize: 15)
  }()

}
