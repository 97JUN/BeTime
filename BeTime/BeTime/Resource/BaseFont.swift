import UIKit

extension UIFont {

  static let title: UIFont = {
    return UIFont.systemFont(ofSize: 25)
  }()

  static let subtitle: UIFont = {
    return UIFont.systemFont(ofSize: 20)
  }()

  static let labelRegular: UIFont = {
    return UIFont.systemFont(ofSize: 15)
  }()

}
